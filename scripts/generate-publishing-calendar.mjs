import { existsSync, mkdirSync, readdirSync, readFileSync, statSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";

const campaignsDir = "content-automation/campaigns";
const outputDir = "content-automation/output";
const csvPath = "content-automation/PUBLISHING_CALENDAR.csv";
const mdPath = "content-automation/PUBLISHING_CALENDAR.md";
const generatedDate = new Intl.DateTimeFormat("en-CA", {
  timeZone: "Asia/Seoul",
  year: "numeric",
  month: "2-digit",
  day: "2-digit"
}).format(new Date());

const readJson = (file) => JSON.parse(readFileSync(file, "utf8"));
const read = (file) => readFileSync(file, "utf8");

const parseCsvLine = (line) => {
  const values = [];
  let value = "";
  let inQuotes = false;

  for (let index = 0; index < line.length; index += 1) {
    const char = line[index];
    const next = line[index + 1];

    if (char === "\"" && inQuotes && next === "\"") {
      value += "\"";
      index += 1;
    } else if (char === "\"") {
      inQuotes = !inQuotes;
    } else if (char === "," && !inQuotes) {
      values.push(value);
      value = "";
    } else {
      value += char;
    }
  }

  values.push(value);
  return values;
};

const parseCsv = (body) => {
  const [headerLine, ...lines] = body.trim().split(/\r?\n/).filter(Boolean);
  const headers = parseCsvLine(headerLine);

  return lines.map((line) => {
    const values = parseCsvLine(line);
    return Object.fromEntries(headers.map((header, index) => [header, values[index] || ""]));
  });
};

const csvValue = (value) => `"${String(value || "").replace(/"/g, '""')}"`;

const campaignFiles = readdirSync(campaignsDir)
  .map((name) => join(campaignsDir, name))
  .filter((file) => statSync(file).isFile())
  .filter((file) => file.endsWith(".json"))
  .sort();

const rows = campaignFiles.flatMap((campaignFile) => {
  const campaign = readJson(campaignFile);
  const queuePath = join(outputDir, `${campaign.date}-${campaign.slug}-publishing-queue.csv`);
  if (!existsSync(queuePath)) return [];

  return parseCsv(read(queuePath)).map((row) => ({
    campaignDate: campaign.date,
    campaign: campaign.slug,
    topic: campaign.topicKo || campaign.topic,
    landingPage: campaign.landingPage,
    publishDate: row.date,
    publishTime: row.time,
    platform: row.platform,
    objective: row.objective,
    assetSpec: row.assetSpec,
    successSignal: row.successSignal,
    guardrail: row.guardrail
  }));
}).sort((left, right) => {
  const leftKey = `${left.publishDate} ${left.publishTime} ${left.platform}`;
  const rightKey = `${right.publishDate} ${right.publishTime} ${right.platform}`;
  return leftKey.localeCompare(rightKey);
});

const headers = [
  "publishDate",
  "publishTime",
  "platform",
  "campaign",
  "topic",
  "objective",
  "assetSpec",
  "successSignal",
  "landingPage",
  "guardrail"
];

const csv = [
  headers.map(csvValue).join(","),
  ...rows.map((row) => headers.map((header) => csvValue(row[header])).join(","))
].join("\n");

const markdownRows = rows
  .map((row) => `| ${row.publishDate} | ${row.publishTime} | ${row.platform} | \`${row.campaign}\` | ${row.objective} | ${row.assetSpec} | ${row.successSignal} |`)
  .join("\n");

const content = `# Grant Labs Publishing Calendar

Last generated: ${generatedDate}

This file is generated by \`npm run content:calendar\`. It combines every campaign publishing queue into one operations calendar for collaborators.

## Calendar

| Date | Time | Platform | Campaign | Objective | Asset | Success signal |
| --- | --- | --- | --- | --- | --- | --- |
${markdownRows}

## Source

- Campaign queue CSV files live in \`content-automation/output/\`.
- Regenerate all campaigns and this calendar with \`npm run content:run:all\`.
- Regenerate only this calendar with \`npm run content:calendar\`.
`;

mkdirSync(dirname(csvPath), { recursive: true });
writeFileSync(csvPath, csv, "utf8");
writeFileSync(mdPath, content, "utf8");

console.log(`Generated publishing calendar: ${csvPath}`);
console.log(`Generated publishing calendar summary: ${mdPath}`);
