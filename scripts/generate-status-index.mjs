import { existsSync, readFileSync, writeFileSync } from "node:fs";

const outputPath = "STATUS_INDEX.md";
const generatedDate = new Intl.DateTimeFormat("en-CA", {
  timeZone: "Asia/Seoul",
  year: "numeric",
  month: "2-digit",
  day: "2-digit"
}).format(new Date());

const read = (file) => readFileSync(file, "utf8");
const contains = (file, marker) => existsSync(file) && read(file).includes(marker);

const docs = [
  ["Start Here", "CLAUDE_HANDOFF.md", "Scope, current state, commands, files, and next steps for a new collaborator"],
  ["Development Log", "DEVELOPMENT_STATUS.md", "Long-running implementation status and chronological development notes"],
  ["Development Journal", "DEVELOPMENT_JOURNAL.md", "Commit-based implementation flow for quick collaborator catch-up"],
  ["Campaign Status", "content-automation/CAMPAIGN_STATUS.md", "Campaign list and generated content output coverage"],
  ["Sitemap", "sitemap.xml", "Indexable page URLs and current lastmod values"],
  ["Deployment Readiness", "DEPLOYMENT_READINESS.md", "Cloudflare Pages readiness checks and open deployment placeholders"],
  ["Deployment Environments", "DEPLOYMENT_ENVIRONMENTS.md", "Preview, production, domain, EmailJS, and smoke-test records"],
  ["Command Guide", "COMMANDS.md", "Standard local, content, deployment, preview, and smoke commands"],
  ["Ops Refresh Report", "OPS_REFRESH_REPORT.md", "Latest daily refresh result across content, sitemap, status documents, and validation"],
  ["Post-publication Review", "content-automation/POST_PUBLICATION_REVIEW.md", "Daily follow-up queue for missing published URLs, first results, learnings, and completed reviews"],
  ["Platform Fit Report", "content-automation/PLATFORM_FIT_REPORT.md", "Channel-specific posting fit checks for Naver, Instagram, YouTube, TikTok, Facebook, and LinkedIn"],
  ["Korean Readability Report", "content-automation/KOREAN_READABILITY_REPORT.md", "Readable Korean, paragraph structure, visible URL, and mojibake checks for platform-ready copy"],
  ["Cloudflare Setup", "CLOUDFLARE_PAGES_SETUP.md", "Cloudflare Pages project settings and first deployment flow"],
  ["QA Checklist", "QA_CHECKLIST.md", "Manual deployment QA and visual checks"],
  ["Rollback Plan", "ROLLBACK_PLAN.md", "Rollback notes for deployment changes"],
  ["Change Log", "CHANGELOG.md", "Concise date-based change summary"]
];

const docRows = docs
  .map(([label, file, purpose]) => `| ${existsSync(file) ? "Ready" : "Missing"} | ${label} | [${file}](${file}) | ${purpose} |`)
  .join("\n");

const guardrails = [
  ["Repository isolation", contains("CLAUDE_HANDOFF.md", "Do not modify the existing `grantlabs-website`")],
  ["Content drift checked in CI", contains(".github/workflows/static-site-check.yml", "git diff --exit-code -- content-automation")],
  ["Sitemap drift checked in CI", contains(".github/workflows/static-site-check.yml", "git diff --exit-code -- sitemap.xml")],
  ["Deployment readiness drift checked in CI", contains(".github/workflows/static-site-check.yml", "git diff --exit-code -- DEPLOYMENT_READINESS.md")],
  ["Development journal drift checked in CI", contains(".github/workflows/static-site-check.yml", "git diff --exit-code -- DEVELOPMENT_JOURNAL.md")],
  ["Local preview checked in CI", contains(".github/workflows/static-site-check.yml", "npm run preview:check")],
  ["Content quality command documented", contains("COMMANDS.md", "npm run check:content")],
  ["Post-publication review command documented", contains("COMMANDS.md", "npm run content:review")],
  ["Platform fit command documented", contains("COMMANDS.md", "npm run content:platform-fit")],
  ["Korean readability command documented", contains("COMMANDS.md", "npm run content:readability")],
  ["Deployment readiness command documented", contains("COMMANDS.md", "npm run deployment:readiness")],
  ["Daily ops refresh command documented", contains("COMMANDS.md", "npm run ops:refresh")]
];

const guardrailRows = guardrails
  .map(([label, passed]) => `| ${passed ? "Ready" : "Missing"} | ${label} |`)
  .join("\n");

const content = `# Grant Labs Status Index

Last generated: ${generatedDate}

This file is generated by \`npm run status:index\`. It is the fastest entry point for collaborators who need to understand the current state without reading every document first.

## Recommended Reading Order

| Status | Step | Document | Purpose |
| --- | --- | --- | --- |
${docRows}

## Guardrail Summary

| Status | Guardrail |
| --- | --- |
${guardrailRows}

## Refresh Commands

\`\`\`bash
npm run content:status
npm run content:review
npm run content:platform-fit
npm run content:readability
npm run sitemap:refresh
npm run deployment:readiness
npm run status:journal
npm run status:index
npm run ops:refresh
npm run check:content
npm run check
\`\`\`

Keep this index committed whenever handoff, campaign, deployment, or command documentation changes.
`;

writeFileSync(outputPath, content, "utf8");
console.log(`Generated status index: ${outputPath}`);
