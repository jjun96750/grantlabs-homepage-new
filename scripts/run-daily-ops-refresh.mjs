import { spawnSync } from "node:child_process";
import { writeFileSync } from "node:fs";

const outputPath = "OPS_REFRESH_REPORT.md";
const generatedDate = new Intl.DateTimeFormat("en-CA", {
  timeZone: "Asia/Seoul",
  year: "numeric",
  month: "2-digit",
  day: "2-digit"
}).format(new Date());

const steps = [
  ["Content automation", ["scripts/run-all-content-automation.mjs"]],
  ["Sitemap refresh", ["scripts/generate-sitemap.mjs"]],
  ["Deployment readiness", ["scripts/generate-deployment-readiness.mjs"]],
  ["Development journal", ["scripts/generate-development-journal.mjs"]],
  ["Status index", ["scripts/generate-status-index.mjs"]],
  ["Content automation check", ["scripts/check-content-automation.mjs"]],
  ["Static site check", ["scripts/check-static-site.mjs"]]
];

const results = [];

for (const [label, args] of steps) {
  console.log(`\nRunning ${label}`);
  const result = spawnSync(process.execPath, args, {
    encoding: "utf8",
    shell: false
  });

  if (result.stdout) process.stdout.write(result.stdout);
  if (result.stderr) process.stderr.write(result.stderr);

  results.push({
    label,
    status: result.status === 0 ? "Passed" : "Failed",
    exitCode: result.status ?? 1
  });

  if (result.status !== 0) {
    writeReport();
    console.error(`${label} failed.`);
    process.exit(result.status || 1);
  }
}

writeReport();
console.log(`\nDaily ops refresh completed: ${outputPath}`);

function writeReport() {
  const rows = results
    .map((result) => `| ${result.label} | ${result.status} | ${result.exitCode} |`)
    .join("\n");

  const content = `# Grant Labs Ops Refresh Report

Date: ${generatedDate}

This file is generated by \`npm run ops:refresh\`. It records the daily refresh sequence so collaborators can see whether content, sitemap, status documents, and validation checks were updated together.

## Refresh Results

| Step | Status | Exit code |
| --- | --- | --- |
${rows}

## Refreshed Outputs

- \`content-automation/TODAY_ACTIONS.md\`
- \`content-automation/UPCOMING_ACTIONS.md\`
- \`content-automation/DAILY_BRIEF.md\`
- \`content-automation/PERFORMANCE_LOG.md\`
- \`content-automation/TRACKED_LINKS.md\`
- \`content-automation/PLATFORM_PLAYBOOK.md\`
- \`content-automation/PLATFORM_POSTING_QA.md\`
- \`content-automation/READY_COPY_INDEX.md\`
- \`content-automation/COPY_QUALITY_REPORT.md\`
- \`sitemap.xml\`
- \`DEPLOYMENT_READINESS.md\`
- \`DEVELOPMENT_JOURNAL.md\`
- \`STATUS_INDEX.md\`

## Command

\`\`\`bash
npm run ops:refresh
\`\`\`
`;

  writeFileSync(outputPath, content, "utf8");
}
