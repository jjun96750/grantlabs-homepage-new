import { existsSync, readFileSync, readdirSync, statSync, writeFileSync } from "node:fs";
import { join } from "node:path";

const calendarCsv = "content-automation/PUBLISHING_CALENDAR.csv";
const campaignsDir = "content-automation/campaigns";
const outputPath = "content-automation/UPCOMING_ACTIONS.md";
const today = new Intl.DateTimeFormat("en-CA", {
  timeZone: "Asia/Seoul",
  year: "numeric",
  month: "2-digit",
  day: "2-digit"
}).format(new Date());

const parseCsvLine = (line) => {
  const values = [];
  let value = "";
  let inQuotes = false;

  for (let index = 0; index < line.length; index += 1) {
    const char = line[index];
    const next = line[index + 1];

    if (char === "\"" && inQuotes && next === "\"") {
      value += "\"";
      index += 1;
    } else if (char === "\"") {
      inQuotes = !inQuotes;
    } else if (char === "," && !inQuotes) {
      values.push(value);
      value = "";
    } else {
      value += char;
    }
  }

  values.push(value);
  return values;
};

const parseCsv = (body) => {
  const [headerLine, ...lines] = body.trim().split(/\r?\n/).filter(Boolean);
  const headers = parseCsvLine(headerLine);
  return lines.map((line) => {
    const values = parseCsvLine(line);
    return Object.fromEntries(headers.map((header, index) => [header, values[index] || ""]));
  });
};

const campaignBySlug = new Map(
  readdirSync(campaignsDir)
    .map((name) => join(campaignsDir, name))
    .filter((file) => statSync(file).isFile())
    .filter((file) => file.endsWith(".json"))
    .map((file) => {
      const campaign = JSON.parse(readFileSync(file, "utf8"));
      return [campaign.slug, campaign];
    })
);

const rows = existsSync(calendarCsv)
  ? parseCsv(readFileSync(calendarCsv, "utf8"))
    .filter((row) => row.publishDate > today)
    .sort((a, b) => `${a.publishDate} ${a.publishTime}`.localeCompare(`${b.publishDate} ${b.publishTime}`))
  : [];

const queueRows = rows.length
  ? rows.map((row) => `| ${row.publishDate} | ${row.publishTime} | ${row.platform} | \`${row.campaign}\` | ${row.objective} | ${row.assetSpec} | ${row.successSignal} |`).join("\n")
  : "| n/a | n/a | n/a | n/a | No upcoming publishing actions found. | n/a | n/a |";

const dateSummaries = [...new Set(rows.map((row) => row.publishDate))]
  .map((date) => {
    const dateRows = rows.filter((row) => row.publishDate === date);
    const platforms = [...new Set(dateRows.map((row) => row.platform))].join(", ");
    const campaigns = [...new Set(dateRows.map((row) => `\`${row.campaign}\``))].join(", ");
    return `| ${date} | ${dateRows.length} | ${platforms} | ${campaigns} |`;
  })
  .join("\n") || "| n/a | 0 | n/a | n/a |";

const sourceRows = [...new Set(rows.map((row) => row.campaign))]
  .sort()
  .map((slug) => {
    const campaign = campaignBySlug.get(slug);
    if (!campaign) return `| \`${slug}\` | missing campaign file | missing | missing | missing | missing |`;

    const base = `${campaign.date}-${campaign.slug}`;
    return `| \`${slug}\` | [Plan](output/${base}.md) | [Assets](output/${base}-asset-briefs.md) | [Captions](output/${base}-caption-pack.md) | [Queue](output/${base}-publishing-queue.md) | [CSV](output/${base}-publishing-queue.csv) |`;
  })
  .join("\n") || "| n/a | n/a | n/a | n/a | n/a | n/a |";

const handoffRows = rows.length
  ? rows.map((row) => `| ${row.publishDate} ${row.publishTime} | ${row.platform} | \`${row.campaign}\` |  |  |  |  |`).join("\n")
  : "| n/a | n/a | n/a |  |  |  |  |";

const content = `# Grant Labs Upcoming Actions

Generated: ${today}

This file is generated by \`npm run content:upcoming\`. It filters \`content-automation/PUBLISHING_CALENDAR.csv\` to show future publishing actions after today's Asia/Seoul date.

## Upcoming Queue

| Date | Time | Platform | Campaign | Objective | Asset | Success signal |
| --- | --- | --- | --- | --- | --- | --- |
${queueRows}

## Date Summary

| Date | Count | Platforms | Campaigns |
| --- | --- | --- | --- |
${dateSummaries}

## Source Files

| Campaign | Plan | Assets | Captions | Queue | CSV |
| --- | --- | --- | --- | --- | --- |
${sourceRows}

## Preparation Log

Use this before the publish date so another collaborator can see what is ready and what still needs production.

| Slot | Platform | Campaign | Asset ready | Caption ready | Owner | Blocker |
| --- | --- | --- | --- | --- | --- | --- |
${handoffRows}

## Refresh

\`\`\`bash
npm run content:run:all
npm run content:upcoming
\`\`\`
`;

writeFileSync(outputPath, content, "utf8");
console.log(`Generated upcoming actions: ${outputPath}`);
