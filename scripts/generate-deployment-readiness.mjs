import { existsSync, readFileSync, writeFileSync } from "node:fs";

const outputPath = "DEPLOYMENT_READINESS.md";

const read = (file) => readFileSync(file, "utf8");
const has = (file, marker) => existsSync(file) && read(file).includes(marker);

const checks = [
  ["Homepage", existsSync("index.html"), "index.html"],
  ["Privacy page", existsSync("privacy.html"), "privacy.html"],
  ["Consultation checklist", existsSync("checklist.html"), "checklist.html"],
  ["404 fallback", existsSync("404.html") && has("_redirects", "/* /404.html 404"), "404.html and _redirects"],
  ["Security headers", has("_headers", "Content-Security-Policy") && has("_headers", "Strict-Transport-Security"), "_headers"],
  ["Sitemap", existsSync("sitemap.xml") && has("robots.txt", "Sitemap:"), "sitemap.xml and robots.txt"],
  ["Local preview server", existsSync("scripts/serve-static.mjs") && has("package.json", "\"serve\""), "npm run serve"],
  ["Preview checker", existsSync("scripts/check-local-preview.mjs") && has("package.json", "\"preview:check\""), "npm run preview:check"],
  ["Deployed smoke checker", existsSync("scripts/check-deployed-site.mjs") && has("package.json", "\"smoke\""), "npm run smoke -- <preview-url>"],
  ["Content automation status", existsSync("content-automation/CAMPAIGN_STATUS.md"), "content-automation/CAMPAIGN_STATUS.md"],
  ["Content automation quality check", existsSync("scripts/check-content-automation.mjs") && has("package.json", "\"check:content\""), "npm run check:content"],
  ["GitHub Actions static workflow", has(".github/workflows/static-site-check.yml", "npm run check") && has(".github/workflows/static-site-check.yml", "npm run preview:check"), ".github/workflows/static-site-check.yml"],
  ["GitHub Actions content workflow", has(".github/workflows/static-site-check.yml", "npm run content:run:all") && has(".github/workflows/static-site-check.yml", "npm run check:content"), ".github/workflows/static-site-check.yml"],
  ["Deployment environment tracker", existsSync("DEPLOYMENT_ENVIRONMENTS.md"), "DEPLOYMENT_ENVIRONMENTS.md"],
  ["Rollback plan", existsSync("ROLLBACK_PLAN.md"), "ROLLBACK_PLAN.md"],
  ["QA checklist", existsSync("QA_CHECKLIST.md"), "QA_CHECKLIST.md"]
];

const deployment = existsSync("DEPLOYMENT_ENVIRONMENTS.md") ? read("DEPLOYMENT_ENVIRONMENTS.md") : "";
const openItems = [
  ["Cloudflare Pages project name", deployment.includes("Project name: TBD")],
  ["Preview URL", deployment.includes("Preview URL: TBD")],
  ["Production URL", deployment.includes("Production URL: TBD")],
  ["Custom domain decision", deployment.includes("Custom domain: TBD")],
  ["Desktop visual QA", deployment.includes("Desktop visual QA: pending")],
  ["Mobile visual QA", deployment.includes("Mobile visual QA: pending")],
  ["EmailJS production-preview test", deployment.includes("EmailJS test: pending")]
].filter(([, isOpen]) => isOpen);

const checkRows = checks
  .map(([label, passed, source]) => `| ${passed ? "Ready" : "Missing"} | ${label} | ${source} |`)
  .join("\n");

const openRows = openItems.length
  ? openItems.map(([label]) => `| Pending | ${label} |`).join("\n")
  : "| Ready | No pending deployment placeholders found. |";

const content = `# Grant Labs Deployment Readiness

Last generated: 2026-06-20

This file is generated by \`npm run deployment:readiness\`. It summarizes whether the isolated Grant Labs homepage repository is ready for Cloudflare Pages preview deployment.

## Readiness Checks

| Status | Area | Source |
| --- | --- | --- |
${checkRows}

## Open Deployment Items

| Status | Item |
| --- | --- |
${openRows}

## Required Local Commands

\`\`\`bash
npm run content:run:all
npm run check:content
npm run check
npm run serve
npm run preview:check
\`\`\`

## Required After Preview URL Exists

\`\`\`bash
npm run smoke -- <preview-url>
\`\`\`

Keep \`DEPLOYMENT_ENVIRONMENTS.md\`, \`DEVELOPMENT_STATUS.md\`, and \`CLAUDE_HANDOFF.md\` updated after every deployment-state change.
`;

writeFileSync(outputPath, content, "utf8");
console.log(`Generated deployment readiness: ${outputPath}`);
