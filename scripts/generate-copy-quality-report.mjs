import { existsSync, mkdirSync, readdirSync, readFileSync, statSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";

const campaignsDir = "content-automation/campaigns";
const readyRoot = "content-automation/output/platform-ready-copy";
const outputPath = "content-automation/COPY_QUALITY_REPORT.md";

const generatedDate = new Intl.DateTimeFormat("en-CA", {
  timeZone: "Asia/Seoul",
  year: "numeric",
  month: "2-digit",
  day: "2-digit"
}).format(new Date());

const readJson = (file) => JSON.parse(readFileSync(file, "utf8"));
const read = (file) => readFileSync(file, "utf8");

const campaignFiles = readdirSync(campaignsDir)
  .map((name) => join(campaignsDir, name))
  .filter((file) => statSync(file).isFile())
  .filter((file) => file.endsWith(".json"))
  .sort();

const platformFiles = [
  ["Naver Blog", "01-naver-blog-copy.txt"],
  ["Instagram Carousel", "02-instagram-carousel-caption.txt"],
  ["Instagram Reels", "03-instagram-reels-script.txt"],
  ["YouTube Shorts", "04-youtube-shorts-script.txt"],
  ["YouTube Long-form", "05-youtube-long-description.txt"],
  ["TikTok", "06-tiktok-caption.txt"],
  ["Facebook Page", "07-facebook-page-post.txt"],
  ["LinkedIn Page", "08-linkedin-page-post.txt"]
];

function checksFor(platform, body) {
  const checks = [];
  const hasUrl = body.includes("https://grantlabs.co.kr/checklist.html");
  const hasMarkdownLink = /\[[^\]]+\]\(https?:\/\//.test(body);
  const paragraphs = body.split(/\n{2,}/).filter((paragraph) => paragraph.trim()).length;

  if (!hasUrl) checks.push("missing raw checklist URL");
  if (hasMarkdownLink) checks.push("contains Markdown link syntax");

  if (platform === "Naver Blog") {
    if (!body.includes("----------------------------------------")) checks.push("missing readable section breaks");
    if (!body.includes("Grant Labs 상담 준비 체크리스트")) checks.push("missing clear checklist CTA block");
    if (body.includes("네이버 에디터")) checks.push("contains editor instruction in public copy");
    if (paragraphs < 10) checks.push("too few readable paragraphs");
  }

  if (platform.includes("Instagram") && !body.includes("캡션에서 클릭되지 않을 수 있습니다")) {
    checks.push("missing Instagram link limitation note");
  }

  if (platform === "TikTok" && !body.includes("프로필 링크")) {
    checks.push("missing TikTok profile/comment link guidance");
  }

  if (platform.includes("YouTube") && !body.includes("Pinned comment")) {
    checks.push("missing YouTube pinned-comment link guidance");
  }

  return {
    status: checks.length ? "Needs review" : "Ready",
    notes: checks.length ? checks.join("; ") : "Readable, URL-forward, and platform-aware"
  };
}

const rows = campaignFiles.flatMap((campaignFile) => {
  const campaign = readJson(campaignFile);
  const baseName = `${campaign.date}-${campaign.slug}`;
  const readyDir = join(readyRoot, baseName);

  return platformFiles.map(([platform, fileName]) => {
    const filePath = join(readyDir, fileName);
    if (!existsSync(filePath)) {
      return {
        date: campaign.date,
        campaign: campaign.slug,
        platform,
        fileName,
        status: "Missing",
        notes: "ready-copy file is missing"
      };
    }

    const result = checksFor(platform, read(filePath));
    return {
      date: campaign.date,
      campaign: campaign.slug,
      platform,
      fileName,
      status: result.status,
      notes: result.notes
    };
  });
});

const readyCount = rows.filter((row) => row.status === "Ready").length;
const reviewCount = rows.length - readyCount;

const tableRows = rows
  .map((row) => `| ${row.date} | \`${row.campaign}\` | ${row.platform} | \`${row.fileName}\` | ${row.status} | ${row.notes} |`)
  .join("\n");

const content = `# Grant Labs Copy Quality Report

Last generated: ${generatedDate}

This file is generated by \`npm run content:quality\`. It checks platform-ready copy for readability, raw URL handling, platform-specific link behavior, and obvious publishing blockers.

## Summary

- Total checks: ${rows.length}
- Ready: ${readyCount}
- Needs review or missing: ${reviewCount}

## Platform Copy Checks

| Date | Campaign | Platform | File | Status | Notes |
| --- | --- | --- | --- | --- | --- |
${tableRows}

## Publishing Standard

- Naver Blog must have readable section breaks, no Markdown links, and a visible raw checklist URL.
- Instagram and TikTok must not assume caption links are clickable.
- YouTube copy should include pinned-comment or description link guidance.
- Facebook and LinkedIn should keep the checklist URL visible in the post body.
`;

mkdirSync(dirname(outputPath), { recursive: true });
writeFileSync(outputPath, content, "utf8");

console.log(`Generated copy quality report: ${outputPath}`);
