import { existsSync, readFileSync, writeFileSync } from "node:fs";

const calendarCsv = "content-automation/PUBLISHING_CALENDAR.csv";
const trackedLinksCsv = "content-automation/TRACKED_LINKS.csv";
const outputPath = "content-automation/PERFORMANCE_LOG.md";
const today = new Intl.DateTimeFormat("en-CA", {
  timeZone: "Asia/Seoul",
  year: "numeric",
  month: "2-digit",
  day: "2-digit"
}).format(new Date());

const parseCsvLine = (line) => {
  const values = [];
  let value = "";
  let inQuotes = false;

  for (let index = 0; index < line.length; index += 1) {
    const char = line[index];
    const next = line[index + 1];

    if (char === "\"" && inQuotes && next === "\"") {
      value += "\"";
      index += 1;
    } else if (char === "\"") {
      inQuotes = !inQuotes;
    } else if (char === "," && !inQuotes) {
      values.push(value);
      value = "";
    } else {
      value += char;
    }
  }

  values.push(value);
  return values;
};

const parseCsv = (body) => {
  const [headerLine, ...lines] = body.trim().split(/\r?\n/).filter(Boolean);
  const headers = parseCsvLine(headerLine);
  return lines.map((line) => {
    const values = parseCsvLine(line);
    return Object.fromEntries(headers.map((header, index) => [header, values[index] || ""]));
  });
};

const rows = existsSync(calendarCsv)
  ? parseCsv(readFileSync(calendarCsv, "utf8"))
    .sort((a, b) => `${a.publishDate} ${a.publishTime}`.localeCompare(`${b.publishDate} ${b.publishTime}`))
  : [];

const trackedRows = existsSync(trackedLinksCsv)
  ? parseCsv(readFileSync(trackedLinksCsv, "utf8"))
  : [];

const trackedLinkByKey = new Map(trackedRows.map((row) => [`${row.campaign}::${row.platform}`, row.trackedUrl]));

const existingPerformanceByKey = existsSync(outputPath)
  ? parsePerformanceRows(readFileSync(outputPath, "utf8"))
  : new Map();

const metricHintFor = (platform) => {
  if (platform === "Naver Blog") return "Search impressions, clicks, inquiry clicks";
  if (platform === "LinkedIn Page") return "Profile visits, saves, qualified comments";
  if (platform === "Instagram Carousel") return "Saves, shares, profile taps";
  if (platform === "Instagram Reels") return "Average watch time, replays, profile taps";
  if (platform === "YouTube Shorts") return "Retention, search discovery, checklist clicks";
  if (platform === "YouTube Long-form") return "Watch time, returning viewers, checklist clicks";
  if (platform === "TikTok") return "Completion rate, comments, profile taps";
  if (platform === "Facebook Page") return "Messages, calls, link clicks";
  return "Reach, engagement, qualified actions";
};

const performanceRows = rows.length
  ? rows.map((row) => {
    const slot = `${row.publishDate} ${row.publishTime}`;
    const status = row.publishDate < today ? "Past - fill URL/results" : row.publishDate === today ? "Today - publish/check" : "Upcoming - prepare";
    const firstCheckDate = row.publishDate < today ? today : row.publishDate;
    const trackedUrl = trackedLinkByKey.get(`${row.campaign}::${row.platform}`) || row.landingPage || "";
    const existing = existingPerformanceByKey.get(`${slot}::${row.platform}::${row.campaign}`);
    const merged = {
      status: existing?.status || status,
      publishedUrl: existing?.publishedUrl || "",
      firstCheckDate: existing?.firstCheckDate || firstCheckDate,
      result: existing?.result || "",
      learnings: existing?.learnings || "",
      repurposeDecision: existing?.repurposeDecision || "",
      followUpOwner: existing?.followUpOwner || ""
    };
    return `| ${slot} | ${row.platform} | \`${row.campaign}\` | ${merged.status} | ${trackedUrl} | ${merged.publishedUrl} | ${metricHintFor(row.platform)} | ${merged.firstCheckDate} | ${merged.result} | ${merged.learnings} | ${merged.repurposeDecision} | ${merged.followUpOwner} |`;
  }).join("\n")
  : "| n/a | n/a | n/a | n/a |  |  | n/a | n/a |  |  |  |  |";

const campaignRows = [...new Set(rows.map((row) => row.campaign))]
  .sort()
  .map((campaign) => {
    const campaignRowsForSlug = rows.filter((row) => row.campaign === campaign);
    const platforms = [...new Set(campaignRowsForSlug.map((row) => row.platform))].join(", ");
    return `| \`${campaign}\` | ${campaignRowsForSlug.length} | ${platforms} |  |  |`;
  })
  .join("\n") || "| n/a | 0 | n/a |  |  |";

const platformRows = [...new Set(rows.map((row) => row.platform))]
  .sort()
  .map((platform) => {
    const platformRowsForName = rows.filter((row) => row.platform === platform);
    const campaigns = [...new Set(platformRowsForName.map((row) => `\`${row.campaign}\``))].join(", ");
    return `| ${platform} | ${platformRowsForName.length} | ${metricHintFor(platform)} | ${campaigns} |  |`;
  })
  .join("\n") || "| n/a | 0 | n/a | n/a |  |";

const content = `# Grant Labs Performance Log

Generated: ${today}

This file is generated by \`npm run content:performance\`. It turns \`content-automation/PUBLISHING_CALENDAR.csv\` into a reusable performance tracking sheet for published social and blog content.

## Post Performance

Fill this after each post goes live. Keep notes short and practical so the next content pass can reuse what worked.

| Slot | Platform | Campaign | Status | Tracked CTA URL | Published URL | Primary metric to check | First check date | Result | Learnings | Repurpose decision | Follow-up owner |
| --- | --- | --- | --- | --- | --- | --- | --- | --- | --- | --- | --- |
${performanceRows}

## Daily Review Queue

Use this section at the start or end of each day.

| Status | Action |
| --- | --- |
| Past - fill URL/results | Add the published URL, first metric, and learning before planning more variants. |
| Today - publish/check | Publish from \`TODAY_ACTIONS.md\`, then add the live URL here. |
| Upcoming - prepare | Confirm assets and platform-ready copy before the slot arrives. |

## Campaign Learnings

Use this section after several posts from the same campaign have shipped.

| Campaign | Planned posts | Platforms | Best-performing angle | Next test |
| --- | --- | --- | --- | --- |
${campaignRows}

## Platform Learnings

Use this section to keep platform-specific lessons visible across campaigns.

| Platform | Planned posts | Metric focus | Campaigns | Format lesson |
| --- | --- | --- | --- | --- |
${platformRows}

## Refresh

\`\`\`bash
npm run content:run:all
npm run content:performance
\`\`\`
`;

writeFileSync(outputPath, content, "utf8");
console.log(`Generated performance log: ${outputPath}`);

function parsePerformanceRows(markdown) {
  const rowsByKey = new Map();
  const lines = markdown.split(/\r?\n/);

  for (const line of lines) {
    if (!line.startsWith("| ") || line.includes("| ---") || line.includes("| Slot |")) continue;
    const cells = line.split("|").slice(1, -1).map((cell) => cell.trim());
    if (cells.length < 12 || cells[0] === "n/a") continue;

    const campaign = cells[2].replace(/`/g, "");
    rowsByKey.set(`${cells[0]}::${cells[1]}::${campaign}`, {
      status: cells[3],
      publishedUrl: cells[5],
      firstCheckDate: cells[7],
      result: cells[8],
      learnings: cells[9],
      repurposeDecision: cells[10],
      followUpOwner: cells[11]
    });
  }

  return rowsByKey;
}
