import { mkdirSync, readFileSync, writeFileSync } from "node:fs";
import { dirname } from "node:path";

const rulesPath = "content-automation/platform-rules.json";
const outputPath = "content-automation/PLATFORM_PLAYBOOK.md";

const generatedDate = new Intl.DateTimeFormat("en-CA", {
  timeZone: "Asia/Seoul",
  year: "numeric",
  month: "2-digit",
  day: "2-digit"
}).format(new Date());

const rules = JSON.parse(readFileSync(rulesPath, "utf8"));

const readyCopyByPlatform = {
  naver_blog: "01-naver-blog-copy.txt",
  instagram_carousel: "02-instagram-carousel-caption.txt",
  instagram_reels: "03-instagram-reels-script.txt",
  youtube_shorts: "04-youtube-shorts-script.txt",
  youtube_long: "05-youtube-long-description.txt",
  tiktok: "06-tiktok-caption.txt",
  facebook_page: "07-facebook-page-post.txt",
  linkedin_page: "08-linkedin-page-post.txt"
};

const qaByPlatform = {
  naver_blog: [
    "Use the raw checklist URL on its own line.",
    "Keep headings specific and search-friendly.",
    "Do not paste Markdown link syntax into the editor.",
    "Keep paragraphs short enough to scan on mobile.",
    "Confirm the top CTA and bottom CTA both point to the tracked checklist link."
  ],
  instagram_carousel: [
    "Keep each slide to one idea.",
    "Make the final slide save-worthy.",
    "Use the tracked checklist link in profile or story follow-up.",
    "Avoid long paragraph captions that duplicate the carousel body.",
    "Make the first slide readable from the profile grid."
  ],
  instagram_reels: [
    "Open with the strongest two-second hook.",
    "Burn captions into the vertical video.",
    "Keep the CTA short enough to read on mobile.",
    "Do not reuse the exact same first frame as YouTube Shorts.",
    "Use a human, practical tone rather than a report-style script."
  ],
  youtube_shorts: [
    "Use a searchable question-style title.",
    "Pin the tracked checklist link in the first comment.",
    "Answer one topic only.",
    "Keep the first sentence understandable without context.",
    "Avoid vague titles such as 'check this first' without the service keyword."
  ],
  youtube_long: [
    "Keep chapter markers in the description.",
    "Pin the tracked checklist link.",
    "Cut Shorts from each major section after publishing.",
    "Keep the description structured enough for search.",
    "Use the blog article as the outline, not as a word-for-word script."
  ],
  tiktok: [
    "Start with the warning or contrarian point.",
    "Keep captions large and plain.",
    "Avoid corporate-sounding intros.",
    "Use one direct problem and one payoff.",
    "Move quickly to the checklist CTA without sounding like an ad."
  ],
  facebook_page: [
    "Lead with a common owner situation.",
    "Include phone/contact CTA when appropriate.",
    "Use a readable image or link preview.",
    "Keep the post referral-friendly for business owners.",
    "Avoid hashtag-heavy endings."
  ],
  linkedin_page: [
    "Lead with the business implication.",
    "Keep the tone executive and evidence-led.",
    "Avoid casual slang and unsupported performance promises.",
    "Tie each claim to readiness, sequence, or evidence.",
    "Use a measured CTA for founders and executives."
  ]
};

const platformSections = rules.platforms.map((platform) => `## ${platform.name}

- Role: ${platform.role}
- Best for: ${platform.bestFor}
- Format: ${platform.format}
- Hook: ${platform.hook}
- Asset: ${platform.asset}
- CTA: ${platform.cta}
- Tone: ${platform.tone}
- Avoid: ${platform.avoid}
- Repurpose rule: ${platform.repurposeRule}
- Ready-copy file: \`${readyCopyByPlatform[platform.id]}\`

### Publishing QA

${(qaByPlatform[platform.id] || []).map((item) => `- ${item}`).join("\n")}
`).join("\n");

const content = `# Grant Labs Platform Playbook

Last generated: ${generatedDate}

This file is generated by \`npm run content:playbook\`. It turns \`content-automation/platform-rules.json\` into a collaborator-facing posting guide so each channel keeps its own role, format, CTA, and quality checks.

## How To Use

- Start with the campaign folder under \`content-automation/output/platform-ready-copy/<date>-<slug>/\`.
- Pick the platform-specific ready-copy file listed below.
- Use the matching tracked URL from \`content-automation/TRACKED_LINKS.md\`.
- After publishing, record the post URL and first signals in \`content-automation/PERFORMANCE_LOG.md\`.

## Universal Readability Rules

- Every channel must have one clear next action.
- Do not paste Markdown link syntax into editors that do not convert it.
- Keep the raw URL visible on Naver Blog and YouTube pinned comments.
- Keep paragraphs short on Naver and LinkedIn; keep captions short on Reels, Shorts, TikTok, and Instagram.
- Use platform-ready copy files instead of copying from the raw caption pack.

${platformSections}
`;

mkdirSync(dirname(outputPath), { recursive: true });
writeFileSync(outputPath, content, "utf8");

console.log(`Generated platform playbook: ${outputPath}`);
