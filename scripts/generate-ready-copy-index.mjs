import { existsSync, readdirSync, readFileSync, statSync, writeFileSync } from "node:fs";
import { join } from "node:path";

const campaignsDir = "content-automation/campaigns";
const outputPath = "content-automation/READY_COPY_INDEX.md";
const readyCopyRoot = "content-automation/output/platform-ready-copy";

const generatedDate = new Intl.DateTimeFormat("en-CA", {
  timeZone: "Asia/Seoul",
  year: "numeric",
  month: "2-digit",
  day: "2-digit"
}).format(new Date());

const readJson = (file) => JSON.parse(readFileSync(file, "utf8"));

const campaignFiles = readdirSync(campaignsDir)
  .map((name) => join(campaignsDir, name))
  .filter((file) => statSync(file).isFile())
  .filter((file) => file.endsWith(".json"))
  .sort();

const fileSet = [
  ["Naver Blog", "01-naver-blog-copy.txt"],
  ["Instagram Carousel", "02-instagram-carousel-caption.txt"],
  ["Instagram Reels", "03-instagram-reels-script.txt"],
  ["YouTube Shorts", "04-youtube-shorts-script.txt"],
  ["YouTube Long-form", "05-youtube-long-description.txt"],
  ["TikTok", "06-tiktok-caption.txt"],
  ["Facebook Page", "07-facebook-page-post.txt"],
  ["LinkedIn Page", "08-linkedin-page-post.txt"]
];

const rows = campaignFiles.map((file) => {
  const campaign = readJson(file);
  const base = `${campaign.date}-${campaign.slug}`;
  const readyDir = `${readyCopyRoot}/${base}`;
  const naverPath = `${readyDir}/01-naver-blog-copy.txt`;
  const summaryPath = `output/${base}-platform-ready-copy.md`;
  const status = existsSync(naverPath.replaceAll("/", "\\")) || existsSync(naverPath)
    ? "Ready"
    : "Missing";

  return `| ${campaign.date} | \`${campaign.slug}\` | ${campaign.topicKo || campaign.topic} | [Summary](${summaryPath}) | [Naver](${naverPath.replace("content-automation/", "")}) | ${status} |`;
});

const fileGuide = fileSet
  .map(([platform, file]) => `| ${platform} | \`${file}\` |`)
  .join("\n");

const content = `# Grant Labs Ready Copy Index

Last generated: ${generatedDate}

This file is generated by \`npm run content:ready-index\`. It helps collaborators find copy-and-paste-ready files without opening raw caption packs.

## Campaign Ready Copy

| Date | Campaign | Topic | Summary | Naver Plain Text | Status |
| --- | --- | --- | --- | --- | --- |
${rows.join("\n")}

## Platform File Map

| Platform | Ready-copy file |
| --- | --- |
${fileGuide}

## Naver Rule

- Use \`01-naver-blog-copy.txt\`.
- Keep the raw checklist URL on its own line.
- Do not paste Markdown link syntax into Naver Blog.
- Keep the generated paragraph breaks.

## Refresh

\`\`\`bash
npm run content:run:all
npm run content:ready-index
\`\`\`
`;

writeFileSync(outputPath, content, "utf8");
console.log(`Generated ready copy index: ${outputPath}`);
