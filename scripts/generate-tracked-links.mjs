import { mkdirSync, readdirSync, readFileSync, statSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";

const campaignsDir = "content-automation/campaigns";
const rulesPath = "content-automation/platform-rules.json";
const csvPath = "content-automation/TRACKED_LINKS.csv";
const mdPath = "content-automation/TRACKED_LINKS.md";

const generatedDate = new Intl.DateTimeFormat("en-CA", {
  timeZone: "Asia/Seoul",
  year: "numeric",
  month: "2-digit",
  day: "2-digit"
}).format(new Date());

const readJson = (file) => JSON.parse(readFileSync(file, "utf8"));
const csvValue = (value) => `"${String(value || "").replace(/"/g, '""')}"`;

const mediumByPlatform = {
  naver_blog: "blog",
  instagram_carousel: "social_carousel",
  instagram_reels: "short_video",
  youtube_shorts: "short_video",
  youtube_long: "video",
  tiktok: "short_video",
  facebook_page: "social_post",
  linkedin_page: "social_post"
};

const sourceByPlatform = {
  naver_blog: "naver",
  instagram_carousel: "instagram",
  instagram_reels: "instagram",
  youtube_shorts: "youtube",
  youtube_long: "youtube",
  tiktok: "tiktok",
  facebook_page: "facebook",
  linkedin_page: "linkedin"
};

function withTrackingUrl(baseUrl, campaignSlug, platformId) {
  const url = new URL(baseUrl);
  url.searchParams.set("utm_source", sourceByPlatform[platformId] || platformId);
  url.searchParams.set("utm_medium", mediumByPlatform[platformId] || "social");
  url.searchParams.set("utm_campaign", campaignSlug);
  url.searchParams.set("utm_content", platformId);
  return url.toString();
}

const rules = readJson(rulesPath);
const campaignFiles = readdirSync(campaignsDir)
  .map((name) => join(campaignsDir, name))
  .filter((file) => statSync(file).isFile())
  .filter((file) => file.endsWith(".json"))
  .sort();

const rows = campaignFiles.flatMap((campaignFile) => {
  const campaign = readJson(campaignFile);

  return rules.platforms.map((platform) => ({
    date: campaign.date,
    campaign: campaign.slug,
    topic: campaign.topicKo || campaign.topic,
    platform: platform.name,
    source: sourceByPlatform[platform.id] || platform.id,
    medium: mediumByPlatform[platform.id] || "social",
    content: platform.id,
    trackedUrl: withTrackingUrl(campaign.landingPage, campaign.slug, platform.id),
    successSignal: platform.name === "Naver Blog"
      ? "Search impressions, checklist clicks, inquiry clicks"
      : platform.cta
  }));
});

const headers = ["date", "platform", "campaign", "topic", "source", "medium", "content", "trackedUrl", "successSignal"];
const csv = [
  headers.map(csvValue).join(","),
  ...rows.map((row) => headers.map((header) => csvValue(row[header])).join(","))
].join("\n");

const groupedRows = rows
  .map((row) => `| ${row.date} | ${row.platform} | \`${row.campaign}\` | \`${row.source}\` | \`${row.medium}\` | [tracked link](${row.trackedUrl}) |`)
  .join("\n");

const content = `# Grant Labs Tracked Links

Last generated: ${generatedDate}

This file is generated by \`npm run content:links\`. It gives every campaign/platform pair a tracked checklist URL for post copy, reporting, and follow-up attribution.

## Link Matrix

| Date | Platform | Campaign | Source | Medium | URL |
| --- | --- | --- | --- | --- | --- |
${groupedRows}

## Usage

- Use the tracked URL for social, video, and blog CTAs when the platform accepts full URLs.
- Keep the raw URL visible in Naver Blog copy so the editor does not convert Markdown syntax.
- Record published post URLs and first metrics in \`content-automation/PERFORMANCE_LOG.md\`.
- Regenerate all campaign content and this matrix with \`npm run content:run:all\`.
`;

mkdirSync(dirname(csvPath), { recursive: true });
writeFileSync(csvPath, csv, "utf8");
writeFileSync(mdPath, content, "utf8");

console.log(`Generated tracked links: ${csvPath}`);
console.log(`Generated tracked links summary: ${mdPath}`);
