import { existsSync, readFileSync, writeFileSync } from "node:fs";

const calendarCsv = "content-automation/PUBLISHING_CALENDAR.csv";
const outputPath = "content-automation/DAILY_BRIEF.md";
const today = new Intl.DateTimeFormat("en-CA", {
  timeZone: "Asia/Seoul",
  year: "numeric",
  month: "2-digit",
  day: "2-digit"
}).format(new Date());

const addDays = (dateText, days) => {
  const date = new Date(`${dateText}T00:00:00+09:00`);
  date.setUTCDate(date.getUTCDate() + days);
  return new Intl.DateTimeFormat("en-CA", {
    timeZone: "Asia/Seoul",
    year: "numeric",
    month: "2-digit",
    day: "2-digit"
  }).format(date);
};

const tomorrow = addDays(today, 1);

const parseCsvLine = (line) => {
  const values = [];
  let value = "";
  let inQuotes = false;

  for (let index = 0; index < line.length; index += 1) {
    const char = line[index];
    const next = line[index + 1];

    if (char === "\"" && inQuotes && next === "\"") {
      value += "\"";
      index += 1;
    } else if (char === "\"") {
      inQuotes = !inQuotes;
    } else if (char === "," && !inQuotes) {
      values.push(value);
      value = "";
    } else {
      value += char;
    }
  }

  values.push(value);
  return values;
};

const parseCsv = (body) => {
  const [headerLine, ...lines] = body.trim().split(/\r?\n/).filter(Boolean);
  const headers = parseCsvLine(headerLine);
  return lines.map((line) => {
    const values = parseCsvLine(line);
    return Object.fromEntries(headers.map((header, index) => [header, values[index] || ""]));
  });
};

const rows = existsSync(calendarCsv)
  ? parseCsv(readFileSync(calendarCsv, "utf8"))
    .sort((a, b) => `${a.publishDate} ${a.publishTime}`.localeCompare(`${b.publishDate} ${b.publishTime}`))
  : [];

const todayRows = rows.filter((row) => row.publishDate === today);
const tomorrowRows = rows.filter((row) => row.publishDate === tomorrow);
const overdueRows = rows.filter((row) => row.publishDate < today);
const futureRows = rows.filter((row) => row.publishDate > today);

const table = (items, emptyMessage) => items.length
  ? items.map((row) => `| ${row.publishDate} | ${row.publishTime} | ${row.platform} | \`${row.campaign}\` | ${row.objective} | ${row.successSignal} |`).join("\n")
  : `| n/a | n/a | n/a | n/a | ${emptyMessage} | n/a |`;

const campaignSummary = (items) => [...new Set(items.map((row) => row.campaign))]
  .sort()
  .map((campaign) => {
    const campaignRows = items.filter((row) => row.campaign === campaign);
    const platforms = [...new Set(campaignRows.map((row) => row.platform))].join(", ");
    return `| \`${campaign}\` | ${campaignRows.length} | ${platforms} |`;
  })
  .join("\n") || "| n/a | 0 | n/a |";

const nextSlot = futureRows[0];
const nextSlotText = nextSlot
  ? `${nextSlot.publishDate} ${nextSlot.publishTime} ${nextSlot.platform} / \`${nextSlot.campaign}\``
  : "No future publishing slots remain in the current calendar.";

const content = `# Grant Labs Daily Brief

Date: ${today}

This file is generated by \`npm run content:brief\`. It summarizes today's publishing focus, tomorrow's preparation queue, and the records collaborators should update.

## Snapshot

| Item | Count / Status |
| --- | --- |
| Today's publishing slots | ${todayRows.length} |
| Tomorrow's preparation slots | ${tomorrowRows.length} |
| Past slots needing performance review | ${overdueRows.length} |
| Future slots remaining | ${futureRows.length} |
| Next scheduled slot | ${nextSlotText} |

## Today Focus

| Date | Time | Platform | Campaign | Objective | Success signal |
| --- | --- | --- | --- | --- | --- |
${table(todayRows, "No same-day publishing actions found.")}

## Campaign Mix Today

| Campaign | Count | Platforms |
| --- | --- | --- |
${campaignSummary(todayRows)}

## Tomorrow Prep

| Date | Time | Platform | Campaign | Objective | Success signal |
| --- | --- | --- | --- | --- | --- |
${table(tomorrowRows, "No tomorrow preparation actions found.")}

## Update These Records

- Fill published URLs and first signals in \`content-automation/TODAY_ACTIONS.md\`.
- Use the platform-ready copy files before publishing, especially Naver's \`01-naver-blog-copy.txt\`.
- Record actual post results and learnings in \`content-automation/PERFORMANCE_LOG.md\`.
- Check asset readiness and blockers in \`content-automation/UPCOMING_ACTIONS.md\`.
- Keep claims diagnostic and preparation-focused; do not guarantee approval, selection, certification, patent registration, or funding.

## Refresh

\`\`\`bash
npm run content:run:all
npm run content:brief
\`\`\`
`;

writeFileSync(outputPath, content, "utf8");
console.log(`Generated daily brief: ${outputPath}`);
