import { existsSync, mkdirSync, readdirSync, readFileSync, statSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";

const campaignsDir = "content-automation/campaigns";
const readyRoot = "content-automation/output/platform-ready-copy";
const outputPath = "content-automation/KOREAN_READABILITY_REPORT.md";

const generatedDate = new Intl.DateTimeFormat("en-CA", {
  timeZone: "Asia/Seoul",
  year: "numeric",
  month: "2-digit",
  day: "2-digit"
}).format(new Date());

const platformFiles = [
  ["Naver Blog", "01-naver-blog-copy.txt", 10],
  ["Instagram Carousel", "02-instagram-carousel-caption.txt", 5],
  ["Instagram Reels", "03-instagram-reels-script.txt", 5],
  ["YouTube Shorts", "04-youtube-shorts-script.txt", 5],
  ["YouTube Long-form", "05-youtube-long-description.txt", 5],
  ["TikTok", "06-tiktok-caption.txt", 5],
  ["Facebook Page", "07-facebook-page-post.txt", 4],
  ["LinkedIn Page", "08-linkedin-page-post.txt", 4]
];

const mojibakeMarkers = ["\uFFFD", "?뺤", "?곷", "?댄", "泥댄", "湲곗", "臾몄", "筌", "�"];
const read = (file) => readFileSync(file, "utf8");
const readJson = (file) => JSON.parse(read(file));

function countMatches(body, regex) {
  return [...body.matchAll(regex)].length;
}

function analyze(body, minimumParagraphs) {
  const paragraphs = body.split(/\n{2,}/).filter((paragraph) => paragraph.trim()).length;
  const hangul = countMatches(body, /[\uAC00-\uD7A3]/gu);
  const suspiciousMarkers = mojibakeMarkers.filter((marker) => body.includes(marker));
  const hasUrl = body.includes("https://grantlabs.co.kr/checklist.html");
  const checks = [];

  if (hangul < 30) checks.push("low Hangul count");
  if (paragraphs < minimumParagraphs) checks.push("too few readable paragraphs");
  if (!hasUrl) checks.push("missing raw checklist URL");
  if (suspiciousMarkers.length) checks.push(`suspected mojibake: ${suspiciousMarkers.join(", ")}`);

  return {
    hangul,
    paragraphs,
    hasUrl,
    status: checks.length ? "Needs readability review" : "Ready",
    notes: checks.length ? checks.join("; ") : "Readable Korean copy with visible CTA URL"
  };
}

const campaignFiles = readdirSync(campaignsDir)
  .map((name) => join(campaignsDir, name))
  .filter((file) => statSync(file).isFile())
  .filter((file) => file.endsWith(".json"))
  .sort();

const rows = [];

for (const campaignFile of campaignFiles) {
  const campaign = readJson(campaignFile);
  const readyDir = join(readyRoot, `${campaign.date}-${campaign.slug}`);

  for (const [platform, fileName, minimumParagraphs] of platformFiles) {
    const filePath = join(readyDir, fileName);
    if (!existsSync(filePath)) {
      rows.push({
        date: campaign.date,
        campaign: campaign.slug,
        platform,
        fileName,
        hangul: 0,
        paragraphs: 0,
        hasUrl: "No",
        status: "Missing",
        notes: "ready-copy file is missing"
      });
      continue;
    }

    const result = analyze(read(filePath), minimumParagraphs);
    rows.push({
      date: campaign.date,
      campaign: campaign.slug,
      platform,
      fileName,
      hangul: result.hangul,
      paragraphs: result.paragraphs,
      hasUrl: result.hasUrl ? "Yes" : "No",
      status: result.status,
      notes: result.notes
    });
  }
}

const readyCount = rows.filter((row) => row.status === "Ready").length;
const reviewCount = rows.length - readyCount;
const tableRows = rows
  .map((row) => `| ${row.date} | \`${row.campaign}\` | ${row.platform} | \`${row.fileName}\` | ${row.hangul} | ${row.paragraphs} | ${row.hasUrl} | ${row.status} | ${row.notes} |`)
  .join("\n");

const content = `# Grant Labs Korean Readability Report

Last generated: ${generatedDate}

This file is generated by \`npm run content:readability\`. It checks platform-ready copy for readable Korean text, paragraph structure, visible checklist URLs, and suspected mojibake before a human posts it.

## Summary

- Total readability checks: ${rows.length}
- Ready: ${readyCount}
- Needs readability review or missing: ${reviewCount}

## Readability Matrix

| Date | Campaign | Platform | File | Hangul chars | Paragraphs | Raw URL | Status | Notes |
| --- | --- | --- | --- | ---: | ---: | --- | --- | --- |
${tableRows}

## Posting Rule

- Naver Blog must keep at least 10 readable paragraph blocks and the raw checklist URL on its own line.
- Short-form channels must still include enough Korean context for the operator to paste without rewriting.
- Any suspected mojibake marker means the post should not be published until the source copy is regenerated.
`;

mkdirSync(dirname(outputPath), { recursive: true });
writeFileSync(outputPath, content, "utf8");

console.log(`Generated Korean readability report: ${outputPath}`);
