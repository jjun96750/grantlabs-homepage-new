import { existsSync, readFileSync, writeFileSync } from "node:fs";

const performanceLogPath = "content-automation/PERFORMANCE_LOG.md";
const outputPath = "content-automation/POST_PUBLICATION_REVIEW.md";

const today = new Intl.DateTimeFormat("en-CA", {
  timeZone: "Asia/Seoul",
  year: "numeric",
  month: "2-digit",
  day: "2-digit"
}).format(new Date());

function parsePerformanceRows(markdown) {
  const rows = [];

  for (const line of markdown.split(/\r?\n/)) {
    if (!line.startsWith("| ") || line.includes("| ---") || line.includes("| Slot |")) continue;
    const cells = line.split("|").slice(1, -1).map((cell) => cell.trim());
    if (cells.length < 12 || cells[0] === "n/a") continue;

    rows.push({
      slot: cells[0],
      platform: cells[1],
      campaign: cells[2].replace(/`/g, ""),
      status: cells[3],
      trackedUrl: cells[4],
      publishedUrl: cells[5],
      metric: cells[6],
      firstCheckDate: cells[7],
      result: cells[8],
      learnings: cells[9],
      repurposeDecision: cells[10],
      followUpOwner: cells[11]
    });
  }

  return rows;
}

const rows = existsSync(performanceLogPath)
  ? parsePerformanceRows(readFileSync(performanceLogPath, "utf8"))
  : [];

const dueRows = rows.filter((row) => row.firstCheckDate <= today);
const missingUrlRows = dueRows.filter((row) => !row.publishedUrl);
const missingResultRows = dueRows.filter((row) => row.publishedUrl && !row.result);
const missingLearningRows = dueRows.filter((row) => row.publishedUrl && row.result && !row.learnings);
const completedRows = dueRows.filter((row) => row.publishedUrl && row.result && row.learnings);

const topMissingUrlRows = missingUrlRows.slice(0, 8);

const platformSummaryRows = summarizeBy(missingUrlRows, "platform")
  .map(([label, count]) => `| ${label} | ${count} |`)
  .join("\n") || "| n/a | 0 |";

const campaignSummaryRows = summarizeBy(missingUrlRows, "campaign")
  .map(([label, count]) => `| \`${label}\` | ${count} |`)
  .join("\n") || "| n/a | 0 |";

const table = (items, empty) => items.length
  ? items.map((row) => `| ${row.slot} | ${row.platform} | \`${row.campaign}\` | ${row.status} | ${row.firstCheckDate} | ${row.publishedUrl || ""} | ${row.metric} | ${row.result || ""} | ${row.learnings || ""} |`)
    .join("\n")
  : `| n/a | n/a | n/a | n/a | n/a | ${empty} | n/a | n/a | n/a |`;

const content = `# Grant Labs Post-publication Review

Date: ${today}

This file is generated by \`npm run content:review\`. It converts \`content-automation/PERFORMANCE_LOG.md\` into a daily follow-up queue so published URLs, first results, and learnings do not get lost between collaborators.

## Summary

- Due for review: ${dueRows.length}
- Missing published URL: ${missingUrlRows.length}
- Missing first result: ${missingResultRows.length}
- Missing learning: ${missingLearningRows.length}
- Completed review rows: ${completedRows.length}

## Priority Follow-up

Start here before scanning the full table. These are the oldest rows without a live URL.

| Slot | Platform | Campaign | Status | First check date | Published URL | Metric | Result | Learnings |
| --- | --- | --- | --- | --- | --- | --- | --- | --- |
${table(topMissingUrlRows, "No missing URLs")}

## Missing URL Summary

| Platform | Missing URLs |
| --- | ---: |
${platformSummaryRows}

| Campaign | Missing URLs |
| --- | ---: |
${campaignSummaryRows}

## Missing Published URL

Add the live post URL first. These rows cannot be evaluated until the URL is recorded.

| Slot | Platform | Campaign | Status | First check date | Published URL | Metric | Result | Learnings |
| --- | --- | --- | --- | --- | --- | --- | --- | --- |
${table(missingUrlRows, "Add published URL")}

## Missing First Result

Add the first signal using the metric guidance. Keep it short enough for the next operator to scan.

| Slot | Platform | Campaign | Status | First check date | Published URL | Metric | Result | Learnings |
| --- | --- | --- | --- | --- | --- | --- | --- | --- |
${table(missingResultRows, "Published URL is present")}

## Missing Learning

Turn the result into one practical next-step observation.

| Slot | Platform | Campaign | Status | First check date | Published URL | Metric | Result | Learnings |
| --- | --- | --- | --- | --- | --- | --- | --- | --- |
${table(missingLearningRows, "Result is present")}

## Completed Reviews

| Slot | Platform | Campaign | Status | First check date | Published URL | Metric | Result | Learnings |
| --- | --- | --- | --- | --- | --- | --- | --- | --- |
${table(completedRows, "No completed reviews yet")}

## Operator Rule

- Record the live URL in \`content-automation/PERFORMANCE_LOG.md\` before checking metrics.
- Capture the first result on or after the first check date.
- Convert each result into a learning or repurpose decision before creating more variants.
`;

writeFileSync(outputPath, content, "utf8");
console.log(`Generated post-publication review: ${outputPath}`);

function summarizeBy(items, field) {
  const counts = new Map();
  for (const item of items) counts.set(item[field], (counts.get(item[field]) || 0) + 1);
  return [...counts.entries()].sort((a, b) => b[1] - a[1] || a[0].localeCompare(b[0]));
}
