import { existsSync, mkdirSync, readdirSync, readFileSync, statSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";

const campaignsDir = "content-automation/campaigns";
const outputDir = "content-automation/output";
const statusPath = "content-automation/CAMPAIGN_STATUS.md";

const outputTypes = [
  ["Plan", ".md"],
  ["Assets", "-asset-briefs.md"],
  ["Captions", "-caption-pack.md"],
  ["Queue CSV", "-publishing-queue.csv"],
  ["Queue MD", "-publishing-queue.md"]
];

const readJson = (file) => JSON.parse(readFileSync(file, "utf8"));

const campaignFiles = readdirSync(campaignsDir)
  .map((name) => join(campaignsDir, name))
  .filter((file) => statSync(file).isFile())
  .filter((file) => file.endsWith(".json"))
  .sort();

const outputLink = (campaign, suffix) => {
  const base = `${campaign.date}-${campaign.slug}`;
  const file = suffix === ".md" ? `${base}.md` : `${base}${suffix}`;
  const path = join(outputDir, file);
  return existsSync(path) ? `[ok](output/${file})` : "missing";
};

const campaigns = campaignFiles.map((file) => {
  const campaign = readJson(file);
  return {
    file,
    ...campaign
  };
}).sort((left, right) => `${left.date}-${left.slug}`.localeCompare(`${right.date}-${right.slug}`));

const latestCampaignDate = campaigns
  .map((campaign) => campaign.date)
  .filter(Boolean)
  .sort()
  .at(-1);

const rows = campaigns.map((campaign) => {
  const outputCells = outputTypes.map(([, suffix]) => outputLink(campaign, suffix)).join(" | ");
  return `| ${campaign.date} | \`${campaign.slug}\` | ${campaign.topicKo || campaign.topic} | ${outputCells} |`;
});

const headers = outputTypes.map(([label]) => label).join(" | ");
const dividers = outputTypes.map(() => "---").join(" | ");

const content = `# Grant Labs Campaign Status

Last generated: ${latestCampaignDate || "unknown"}

This file is generated by \`npm run content:status\`. It helps collaborators confirm which campaigns exist and whether each expected output file is present.

## Campaign Outputs

| Date | Campaign | Focus | ${headers} |
| --- | --- | --- | ${dividers} |
${rows.join("\n")}

## Commands

\`\`\`bash
npm run content:status
npm run content:run:all
npm run check:content
npm run check
\`\`\`
`;

mkdirSync(dirname(statusPath), { recursive: true });
writeFileSync(statusPath, content, "utf8");

console.log(`Generated campaign status: ${statusPath}`);
