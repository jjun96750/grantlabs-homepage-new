import { spawnSync } from "node:child_process";
import { writeFileSync } from "node:fs";

const outputPath = "DEVELOPMENT_JOURNAL.md";
const maxEntries = 40;
const logEntriesToRead = 100;
const safeDirectory = process.cwd().replaceAll("\\", "/");
const skippedSubjectPrefixes = [
  "Refresh development journal",
  "Refresh handoff"
];

const result = spawnSync("git", [
  "-c",
  `safe.directory=${safeDirectory}`,
  "log",
  `-${logEntriesToRead}`,
  "--date=short",
  "--pretty=format:%h%x09%ad%x09%s"
], {
  encoding: "utf8",
  shell: false
});

if (result.status !== 0) {
  console.error(result.stderr || "Unable to read git history.");
  process.exit(result.status || 1);
}

const rows = result.stdout
  .trim()
  .split("\n")
  .filter(Boolean)
  .map((line) => {
    const [sha, date, ...subjectParts] = line.split("\t");
    const subject = subjectParts.join("\t");
    return { sha, date, subject };
  })
  .filter(({ subject }) => !skippedSubjectPrefixes.some((prefix) => subject.startsWith(prefix)))
  .slice(0, maxEntries)
  .map(({ sha, date, subject }) => `| ${date} | \`${sha}\` | ${subject.replaceAll("|", "\\|")} |`)
  .join("\n") || "| n/a | n/a | No implementation commits found. |";

const content = `# Grant Labs Development Journal

Last generated: 2026-06-20

This file is generated by \`npm run status:journal\`. It summarizes recent implementation commits so collaborators can quickly see how the project has progressed.

## Recent Implementation Flow

| Date | Commit | Summary |
| --- | --- | --- |
${rows}

## Related Status Documents

- [STATUS_INDEX.md](STATUS_INDEX.md)
- [DEVELOPMENT_STATUS.md](DEVELOPMENT_STATUS.md)
- [CLAUDE_HANDOFF.md](CLAUDE_HANDOFF.md)
- [CHANGELOG.md](CHANGELOG.md)
`;

writeFileSync(outputPath, content, "utf8");
console.log(`Generated development journal: ${outputPath}`);
